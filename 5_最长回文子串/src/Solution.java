class Solution {
    public String longestPalindrome(String s) {
        int len = s.length();
        if (len < 2) {
            return s;
        }

        int maxLen = 1;
        String res = s.substring(0, 1);

        for (int left = 0; left < len - 1; left++) {
            for (int right = left + 1; right < len; right++) {
                if (right - left + 1 > maxLen && isRev(s, left, right)) {
                    maxLen = right - left + 1;
                    res = s.substring(left, right + 1);
                }
            }
        }
        return res;
    }

    private boolean isRev(String s, int left, int right) {
        while (left < right) {
            if (s.charAt(left) != s.charAt(right)) {
                return false;
            }
            left++;
            right--;
        }
        return true;
    }
}
